    private void PerfilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PerfilActionPerformed
        String caminhoArquivo = "src\\\\main\\\\java\\\\com\\\\mycompany\\\\sistema\\\\guarda\\\\arquivo\\\\Recrutas.xlsx";
        File arquivo = new File(caminhoArquivo);
        Workbook workbook;
        Sheet sheet;

        if (arquivo.exists()) {
            try (FileInputStream fis = new FileInputStream(arquivo)) {
                workbook = new XSSFWorkbook(fis);
                sheet = workbook.getSheetAt(0);
            } catch (IOException e) {
                e.printStackTrace();
                return;
            }
        } else {
            workbook = new XSSFWorkbook();
            sheet = workbook.createSheet("Alunos");

            // Criar cabe√ßalhos
            Row headerRow = sheet.createRow(0);
            headerRow.createCell(0).setCellValue("Nome");
            headerRow.createCell(1).setCellValue("Turma");
            headerRow.createCell(2).setCellValue("Turno");
        }

        int rowNum = sheet.getLastRowNum() + 1;

        for (Usuario aluno : recrutas) {
            Row row = sheet.createRow(rowNum++);
            row.createCell(0).setCellValue(aluno.getNome());
            row.createCell(1).setCellValue(aluno.getTurma());
            row.createCell(2).setCellValue(aluno.getTurno());
        }

        try (FileOutputStream fos = new FileOutputStream(caminhoArquivo)) {
            workbook.write(fos);
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            try {
                workbook.close();
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_PerfilActionPerformed

    private void ReprovadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReprovadoActionPerformed
        if(Tabela.getSelectedRow() != -1){
            firestore.eliminar(tipo, recrutas.get(Tabela.getSelectedRow()).getNome());
            recrutas.remove(Tabela.getSelectedRow());
            ids.remove(Tabela.getSelectedRow());
        }else{
            System.out.println("Nada Selecinado!");
        }
    }//GEN-LAST:event_ReprovadoActionPerformed

    private void AprovadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AprovadoActionPerformed
        if(Tabela.getSelectedRow() != -1){
            //recrutas.get(Tabela.getSelectedRow()).setNivel(1);
            recrutas.get(Tabela.getSelectedRow()).setLogin(recrutas.get(Tabela.getSelectedRow()).getNome());
            recrutas.get(Tabela.getSelectedRow()).setSenha("123");
            recrutas.get(Tabela.getSelectedRow()).verificarDivisao();
            firestore.guardarOuAtualizar(tipo, recrutas.get(Tabela.getSelectedRow()).getNome(), UsuarioCovert.formatarListaParaString(recrutas.get(Tabela.getSelectedRow())));
            firestore.eliminar("Alunos", recrutas.get(Tabela.getSelectedRow()).getNome());
            recrutas.remove(Tabela.getSelectedRow());
        }else{
            System.out.println("Nada Selecinado!");
        }
    }//GEN-LAST:event_AprovadoActionPerformed


