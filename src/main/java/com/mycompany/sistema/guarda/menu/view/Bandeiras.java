    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarActionPerformed
        bandeiras.get(tabela.getSelectedRow()).setNome(Nome.getText());
        bandeiras.get(tabela.getSelectedRow()).setTipo((String) tipo.getSelectedItem());
        bandeiras.get(tabela.getSelectedRow()).setStatus((String) Status.getSelectedItem());
        
        if(tabela.getSelectedRow() != -1){
            tabela.setValueAt(Nome.getText(), tabela.getSelectedRow(), 0);
            tabela.setValueAt(tipo.getSelectedItem(), tabela.getSelectedRow(), 1);
            tabela.setValueAt(Status.getSelectedItem(), tabela.getSelectedRow(), 2);
        }
        firestore.guardarOuAtualizar(tip, bandeiras.get(tabela.getSelectedRow()).getNome(), BandeiraCovert.formatarListaParaString(bandeiras.get(tabela.getSelectedRow())));
    }//GEN-LAST:event_EditarActionPerformed

