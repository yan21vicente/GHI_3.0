    private void InserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InserirActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        Object[] dados = {Nome.getText(),tipo.getSelectedItem(),Status.getSelectedItem()};
        Bandeira dato = new Bandeira(Nome.getText(),  (String)tipo.getSelectedItem(), (String)Status.getSelectedItem());
        modelo.addRow(dados);
        bandeiras.add(dato);
        firestore.guardarOuAtualizar(tip, dato.getNome(), BandeiraCovert.formatarListaParaString(dato));
        
    }//GEN-LAST:event_InserirActionPerformed

    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
        if(tabela.getSelectedRow() != -1){
            DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
            firestore.eliminar(tip, bandeiras.get(tabela.getSelectedRow()).getNome());
            bandeiras.remove(tabela.getSelectedRow());
            modelo.removeRow(tabela.getSelectedRow());
        }else{
            System.out.println("Nada Selecinado!");
        }
    }//GEN-LAST:event_ExcluirActionPerformed

    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        if(tabela.getSelectedRow() != -1){
            Nome.setText(tabela.getValueAt(tabela.getSelectedRow(), 0).toString());
            tipo.setSelectedItem(tabela.getValueAt(tabela.getSelectedRow(), 1));
            Status.setSelectedItem(tabela.getValueAt(tabela.getSelectedRow(), 1));
            setImageToLabel(Imagem, tabela.getValueAt(tabela.getSelectedRow(), 0).toString()+".png");
        }
    }//GEN-LAST:event_tabelaMouseClicked

    private void ImagemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ImagemMouseClicked
        File destino = new File("src/main/java/imagens/");

        // Verifique se o destino existe, caso contrário, crie-o
        if (!destino.exists()) {
            destino.mkdirs();
        }

        // Chama a função para mover e renomear a imagem
        moverERenomearImagem(destino, Nome.getText());
    }//GEN-LAST:event_ImagemMouseClicked

